            int i = 0
            Console.WriteLine("i++ = " + i++); // increment i by 1, but call i first
            Console.WriteLine("++1 = " + ++i); // increment i by 1, but call i after
            Console.WriteLine("i-- = " + i--); // decrement i by 1, but call i first
            Console.WriteLine("--i = " + --i); // decrement i by 1, but call i after
            Console.WriteLine("i += 3 = " + i += 3); // i = i + 3
            Console.WriteLine("i -= 3 = " + i -= 3); // i = i - 3
            Console.WriteLine("i *= 2 = " + i *= 2); // i = i * 2
            Console.WriteLine("i /= 2 = " + i /= 2); // i = i / 2
            Console.WriteLine("i %= 2 = " + i %= 2); // i = i % 2