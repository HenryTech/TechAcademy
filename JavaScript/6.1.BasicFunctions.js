console.log('Arf');
console.log('Woof');
console.log('Meow');
console.log('Moooooooooooo'); // Animal noises: very important

function speak() {
    console.log('Arf');
    console.log('Woof');
    console.log('Meow');
    console.log('Moooooooooooo');
} // Now we have a function called speak() that will do all of these commands every time we invoke it.

speak(); // Now it's so much easier to make animal noises appear on my screen!


